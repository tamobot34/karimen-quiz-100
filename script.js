let current = 0;
let score = 0;
let answered = false;

const progressEl = document.getElementById("progress");
const scoreEl = document.getElementById("score");
const questionEl = document.getElementById("question");
const feedbackEl = document.getElementById("feedback");
const nextBtn = document.getElementById("next");
const resultEl = document.getElementById("result");
const answerButtons = [...document.querySelectorAll("#answer-buttons .btn")];

function renderQuestion() {
  answered = false;
  feedbackEl.className = "feedback hidden";
  feedbackEl.innerHTML = "";
  nextBtn.disabled = true;
  answerButtons.forEach((b) => (b.disabled = false));

  const item = quizData[current];
  progressEl.textContent = `${current + 1} / ${quizData.length}`;
  scoreEl.textContent = `正解 ${score}`;
  questionEl.textContent = `Q${current + 1}. ${item.q}`;
}

function handleAnswer(userAnswer) {
  if (answered) return;
  answered = true;

  const item = quizData[current];
  const ok = userAnswer === item.a;
  if (ok) score++;

  scoreEl.textContent = `正解 ${score}`;
  feedbackEl.className = `feedback ${ok ? "ok" : "ng"}`;
  feedbackEl.innerHTML = `
    <strong>${ok ? "正解！" : "不正解"}</strong><br>
    正しい答え：<b>${item.a === "o" ? "○" : "×"}</b><br>
    ${item.e}
  `;

  answerButtons.forEach((b) => (b.disabled = true));
  nextBtn.disabled = false;
}

answerButtons.forEach((btn) => {
  btn.addEventListener("click", () => handleAnswer(btn.dataset.answer));
});

nextBtn.addEventListener("click", () => {
  current++;
  if (current >= quizData.length) {
    showResult();
  } else {
    renderQuestion();
  }
});

function showResult() {
  document.getElementById("quiz-card").classList.add("hidden");
  const rate = Math.round((score / quizData.length) * 100);
  let msg = "ええ感じ！この調子でいこ。";
  if (rate < 60) msg = "ここから伸びるで。解説を見直してもう1周！";
  if (rate >= 90) msg = "めっちゃ強い。ほぼ仕上がってる！";

  resultEl.classList.remove("hidden");
  resultEl.innerHTML = `
    <h2>結果発表</h2>
    <p><strong>${score} / ${quizData.length} 問正解（${rate}%）</strong></p>
    <p>${msg}</p>
    <button id="retry" class="btn ghost">もう一回チャレンジ</button>
  `;

  document.getElementById("retry").addEventListener("click", () => {
    current = 0;
    score = 0;
    resultEl.classList.add("hidden");
    document.getElementById("quiz-card").classList.remove("hidden");
    renderQuestion();
  });
}

renderQuestion();
